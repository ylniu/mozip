subroutine disp (n,d,v)
	use kinds
	implicit none
	!----------------------------------------------------------------------------
	!
	! Input  variables
	!
	integer , intent(in ) :: n
	real(DP), intent(in ) :: d(3)

	!----------------------------------------------------------------------------
	!
	! Output variables
	!
	real(DP), intent(out) :: v(3,n)
	!----------------------------------------------------------------------------
	!
	! Local  variables
	!
	integer  :: i
	!----------------------------------------------------------------------------
	do i= 1, n
		v(:,i) = v(:,i) + d
	end do
	!----------------------------------------------------------------------------
	return
end
