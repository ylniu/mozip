subroutine lowercase(a)
	implicit none
	!----------------------------------------------------------------------------
	character(*) :: a
	!----------------------------------------------------------------------------
	integer      :: la, i, j, ia, JA, JZ, iJ
	!----------------------------------------------------------------------------
	la=len_trim(a)
	!----------------------------------------------------------------------------
	ia=iachar("a")
	JA=iachar("A")
	JZ=iachar("Z")
	iJ=ia-JA
	do i=1, la
		j=iachar(a(i:i))
		if ( j>=JA .and. j<=JZ ) then
			a(i:i) = char(j+iJ)
		end if
	end do
	!----------------------------------------------------------------------------
	return
end subroutine
