subroutine reduce_int(n, a)
	implicit none
	!----------------------------------------------------------------------------
	! Input  variable
	!
	integer, intent(in   ) :: n
	!----------------------------------------------------------------------------
	! Output variable
	!
	integer, intent(inout) :: a(n)
	!----------------------------------------------------------------------------
	! Local  variables
	!
	integer                :: i, j, m, imin, reduce_num
	integer, allocatable   :: b(:)
	logical                :: find
	!----------------------------------------------------------------------------
	m=0
	do i=1, n
		if (a(i)/=0) m=m+1
	end do
	!----------------------------------------------------------------------------
	if (m==0) return
	!----------------------------------------------------------------------------
	allocate(b(m))
	!----------------------------------------------------------------------------
	j=0
	do i=1, n
		if (a(i)/=0) then
			j    = j+1
			b(j) = abs (a(i))
		end if
	end do
	!----------------------------------------------------------------------------
	imin=b(1)
	do j=2, m
		if (imin>b(j)) then
			imin=b(j)
		end if
	end do
	!----------------------------------------------------------------------------
	do i=imin,1,-1
		find=.true.
		do j=1, m
			if (mod(b(j),i)/=0) then
				find=.false.
				exit
			end if
		end do
		if (find) then
			reduce_num=i
			exit
		end if
	end do
	!----------------------------------------------------------------------------
	a=a/reduce_num
	!----------------------------------------------------------------------------
	deallocate(b)
	!----------------------------------------------------------------------------
	return
end subroutine
