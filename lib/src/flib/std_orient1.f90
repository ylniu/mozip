subroutine std_orient1(n, x)
	use kinds
	use symmetry1, only: symm_op, npg
	implicit none
	!----------------------------------------------------------------------------
	! Input  variables
	!
	integer , intent(in ) :: n
	!----------------------------------------------------------------------------
	! Output variables
	!
	real(DP), intent(out) :: x(3,n)
	!----------------------------------------------------------------------------
	integer               :: i
	integer               :: isplxy, isplxz
	real(DP)              :: eps
	real(DP)              :: c4x(3,3), c4y(3,3), c4z(3,3)
	!----------------------------------------------------------------------------
	! C4z : symm_op(1, 1,  9)
	! C4y : symm_op(1, 1, 10)
	! C4x : symm_op(1, 1, 11)
	!
	!----------------------------------------------------------------------------
	eps = 1.D-6
	c4z = symm_op(:,:, 9)
	c4y = symm_op(:,:,10)
	c4x = symm_op(:,:,11)
	!----------------------------------------------------------------------------
	if (npg==4) then
		!-------------------------------------------------------------------------
		! CS(y) -> CS(z)
		!
		call rotn(n, c4x, x)
	else if (npg==5) then
		!-------------------------------------------------------------------------
		! CS(x) -> CS(z)
		!
		call rotn(n, c4y, x)
	else if (npg==7) then
		!-------------------------------------------------------------------------
		! C2(y) -> CS(z)
		!
		call rotn(n, c4x, x)
	else if (npg==8) then
		!-------------------------------------------------------------------------
		! C2(x) -> CS(z)
		!
		call rotn(n, c4y, x)
	else if(npg==11) then
		!-------------------------------------------------------------------------
		! C2V(y) -> C2V(z)
		!
		call rotn(n, c4x, x)
	else if (npg==12) then
		!-------------------------------------------------------------------------
		! C2V(x) -> C2V(z)
		!
		call rotn(n, c4y, x)
	else if (npg==14) then
		!-------------------------------------------------------------------------
		! C2H(y) -> C2H(z)
		!
		call rotn(n, c4x, x)
	else if (npg==15) then
		!-------------------------------------------------------------------------
		! C2H(x) -> C2H(z)
		!
		call rotn(n, c4y, x)
	end if
	!----------------------------------------------------------------------------
	! d2h/c2v if planar make sure its yz plane
	!
	if (npg==16 .or. npg==10 .or. npg==11 .or. npg==12) then
		isplxz= 1
		isplxy= 1
		do i= 1, n
			if (abs(x(2,i))>eps) isplxz= 0
			if (abs(x(3,i))>eps) isplxy= 0
		end do
		if (isplxz==1) call rotn(n, c4z, x)
		if (isplxy==1) call rotn(n, c4y, x)
	end if
	!----------------------------------------------------------------------------
	return
end subroutine
