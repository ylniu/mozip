subroutine a_to_lattice_constants(aa, a, b, c, alpha, beta, gamma)
	use kinds, only: DP
	implicit none
	!----------------------------------------------------------------------------
	real(DP) :: aa(3,3)
	real(DP) :: a, b, c, alpha, beta, gamma
	!----------------------------------------------------------------------------
	real(DP) :: a1(3), a2(3), a3(3)
	real(DP) :: PI
	!----------------------------------------------------------------------------
	PI = acos(-1.D0)
	a1 = aa(:,1)
	a2 = aa(:,2)
	a3 = aa(:,3)
	!----------------------------------------------------------------------------
	a = sqrt(a1(1)**2 + a1(2)**2 + a1(3)**2)
	b = sqrt(a2(1)**2 + a2(2)**2 + a2(3)**2)
	c = sqrt(a3(1)**2 + a3(2)**2 + a3(3)**2)
	!----------------------------------------------------------------------------
	alpha = acos(dot_product(a2, a3) / b / c) / PI * 180.D0
	beta  = acos(dot_product(a3, a1) / c / a) / PI * 180.D0
	gamma = acos(dot_product(a1, a2) / a / b) / PI * 180.D0
	!----------------------------------------------------------------------------
	return
end subroutine