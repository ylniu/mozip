subroutine upcase(a)
	implicit none
	!----------------------------------------------------------------------------
	character(*) :: a
	!----------------------------------------------------------------------------
	integer      :: la, i, j, ia, iz, JA, iJ
	!----------------------------------------------------------------------------
	la=len_trim(a)
	!----------------------------------------------------------------------------
	ia=iachar("a")
	iz=iachar("z")
	JA=iachar("A")
	iJ=ia-JA
	do i=1, la
		j=iachar(a(i:i))
		if ( j>=ia .and. j<=iz ) then
			a(i:i) = char(j-iJ)
		end if
	end do
	!----------------------------------------------------------------------------
	return
end subroutine
