function is_number(a)
	implicit none
	!----------------------------------------------------------------------------
	! Input  variable
	!
	character(*) :: a
	!----------------------------------------------------------------------------
	! Output variable
	!
	logical      :: is_number
	!----------------------------------------------------------------------------
	! Local  variable
	!
	integer      :: i, j, n
	!----------------------------------------------------------------------------
	a=trim(adjustl(a))
	n=len(trim(a))
	!----------------------------------------------------------------------------
	if (n==0) then
		is_number = .false.
		return
	end if
	!----------------------------------------------------------------------------
	is_number = .true.
	do i=1, n
		j=iachar(a(i:i))
		if ( j<48 .or. j>57 ) then
			is_number = .false.
			return
		end if
	end do
	!----------------------------------------------------------------------------
	return
end function
