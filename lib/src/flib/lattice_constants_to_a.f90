subroutine lattice_constants_to_a(aa, a, b, c, alpha, beta, gamma)
	!----------------------------------------------------------------------------
	! alpha, beta, gamma from 0 to 180
	!
	use kinds, only: DP
	implicit none
	!----------------------------------------------------------------------------
	! Input variables
	!
	real(DP), intent( in) :: a, b, c, alpha, beta, gamma
	!----------------------------------------------------------------------------
	! Output variables
	!
	real(DP), intent(out) :: aa(3,3)
	!----------------------------------------------------------------------------
	! Local variables
	!
	real(DP) :: alpha1, beta1, gamma1, a1(3), a2(3), a3(3)
	real(DP) :: PI
	!----------------------------------------------------------------------------
	PI      = acos(-1.D0)
	alpha1  = alpha * PI / 180
	beta1   = beta  * PI / 180
	gamma1  = gamma * PI / 180
	!----------------------------------------------------------------------------
	a1      = 0.D0
	a2      = 0.D0
	a3      = 0.D0
	!----------------------------------------------------------------------------
	a1(1)   = a
	!
	a2(1)   = b * cos(gamma1)
	a2(2)   = b * sin(gamma1)
	!
	a3(1)   = c * cos(beta1 )
	a3(2)   = ( b * c * cos(alpha1) - a2(1) * a3(1) ) / a2(2)
	a3(3)   = sqrt(c**2 - a3(1)**2 - a3(2)**2)
	!----------------------------------------------------------------------------
	aa(:,1) = a1
	aa(:,2) = a2
	aa(:,3) = a3
	!----------------------------------------------------------------------------
	return
end subroutine
