subroutine qm_wien2k_struct_natom(fstruct, natom, info)
	!----------------------------------------------------------------------------
	integer      :: natom, info
	character(*) :: fstruct
	!----------------------------------------------------------------------------
	integer      :: fid
	!----------------------------------------------------------------------------
	call get_free_fid(fid)
	!----------------------------------------------------------------------------
	info=-1
	open(fid, file=fstruct, status="old")
		read(fid,*)
		read(fid,'(27x, i3)') natom
	close(fid)
	info=1
	!----------------------------------------------------------------------------
	return
end subroutine
