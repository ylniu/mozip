subroutine del_space(a)
	!----------------------------------------------------------------------------
	implicit none
	!----------------------------------------------------------------------------
	character(*), intent(inout) :: a
	!----------------------------------------------------------------------------
	! Local variables
	!
	integer                     :: i, la, k
	character(len=len(a))       :: b
	!----------------------------------------------------------------------------
	a=trim(adjustl(a))
	la=len_trim(a)
	!
	b=''
	if (la==0) return
	!
	k=0
	do i=1, la
		if (iachar(a(i:i))/=32) then
			k=k+1
			b(k:k)=a(i:i)
		end if
	end do
	a=b
	!----------------------------------------------------------------------------
	return
end subroutine
