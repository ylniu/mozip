subroutine normalize_area(n, x,  y)
	use kinds, only: DP
	implicit none
	!--------------------------------------------------------------------
	integer , intent(in   ) :: n
	real(DP), intent(in   ) :: x(n)
	!--------------------------------------------------------------------
	real(DP), intent(inout) :: y(n)
	!--------------------------------------------------------------------
	integer                 :: i
	real(DP)                :: s
	!--------------------------------------------------------------------
	s = 0.0_DP
	do i=1, n-1
		s = s + 0.5_DP * ( y(i) + y(i+1) ) * ( x(i+1) - x(i) )
	end do
	!--------------------------------------------------------------------
	y = y / s
	!--------------------------------------------------------------------
	return
	!--------------------------------------------------------------------
end subroutine