subroutine get_b(x1, x2, b)
	use kinds, only:DP
	implicit none
	!--------------------------------------------------------------------
	real(DP) :: x1(3), x2(3), b
	real(DP) :: dx(3)
	!--------------------------------------------------------------------
	dx = x1 - x2
	b = sqrt(dot_product(dx, dx))
	!--------------------------------------------------------------------
end subroutine